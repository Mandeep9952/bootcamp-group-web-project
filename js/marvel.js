class character{
	constructor(name,description,image){
  	this.name=name;
    this.description=description;
    this.image=image;
  }
  //getName is method , name is variable
  getName(){
    return this.name;
    
  }
  getDescription(){
  	return this.description;
  }
  getImage(){
   	return this.image;
  }

}

